    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        int qtdGuerreiros=1;
        String nome = this.jTextFieldNome.getText();
        int forca = Integer.parseInt(this.jTextFieldForca.getText());
        int agilidade = Integer.parseInt(this.jTextFieldAgilidade.getText());
        int inteligencia = Integer.parseInt(this.jTextFieldInteligencia.getText());
        
        Guerreiro novoGuerreiro;
        
        int classSelec = this.jClassSelector.getSelectedIndex();
        switch (classSelec){
            case 0:
                novoGuerreiro = new Elfo(this.conector,nome, forca, agilidade, inteligencia);
                novoGuerreiro.setarComportamentoHabilidadeEsp(new AumentarAgilidade(novoGuerreiro));
                qtdGuerreiros = conector.addGuerreiro(novoGuerreiro);
              break;
            case 1:
                novoGuerreiro = new Mago(this.conector,nome, forca, agilidade, inteligencia);
                novoGuerreiro.setarComportamentoHabilidadeEsp(new Regenerar(novoGuerreiro));
                qtdGuerreiros = conector.addGuerreiro(novoGuerreiro);
              break;
            case 2:
                novoGuerreiro = new Orc(this.conector,nome, forca, agilidade, inteligencia);
                novoGuerreiro.setarComportamentoHabilidadeEsp(new AumentarDefesa(novoGuerreiro));
                qtdGuerreiros = conector.addGuerreiro(novoGuerreiro);
              break;
        }
        int selecionado = this.jListArmas.getSelectedIndex();
        
        switch(selecionado){
            case 0:
                conector.darArma(qtdGuerreiros, new Cajado());
                break;
            case 1:
                conector.darArma(qtdGuerreiros, new Clava());
                break;
            case 2:
                conector.darArma(qtdGuerreiros, new Machado());
                break;
            case 3:
                conector.darArma(qtdGuerreiros, new Arco());
                break;
        }
        //Consertar a passagem de vida
        this.janelaPai.loadGame(qtdGuerreiros);
        
        if(qtdGuerreiros >= 2){
            this.janelaPai.switchView(3);
            
            //conector.
        }
        else{
            this.resetScreen();
            this.jLabelJogador.setText("Jogador 2");
        }
        
    }                                        

