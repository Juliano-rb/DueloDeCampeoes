    private void addPtsRestantes(int qtd){
        int ptsAntigos = Integer.parseInt(this.jTextFieldPontos.getText());
        String novosPtsRestantes = Integer.toString(ptsAntigos+qtd);
        this.jTextFieldPontos.setText(novosPtsRestantes);
    }
    
    private void subPtsRestantes(int qtd){
        int ptsAntigos = Integer.parseInt(this.jTextFieldPontos.getText());
        String novosPtsRestantes = Integer.toString(ptsAntigos-qtd);
        this.jTextFieldPontos.setText(novosPtsRestantes);        
    }
    
    private int getPtsRestantes(){
        int ptsAntigos = Integer.parseInt(this.jTextFieldPontos.getText());
        return ptsAntigos;
    }
    
    
